/** This enum serves the purpose of facilitating ordering.
 * In this enum, there are four values defined:
 * `cronologicalAscending`, `cronologicalDecending`, `alphabeticalAscending`, and `alphabeticalDescending`.
 * */
enum EOrderingParam {
  cronologicalAscending = 'cronological ascending',
  cronologicalDecending = 'cronological decending',
  alphabeticalAscending = 'alphabetical ascending',
  alphabeticalDescending = 'alphabetical descending',
}

export default EOrderingParam;
